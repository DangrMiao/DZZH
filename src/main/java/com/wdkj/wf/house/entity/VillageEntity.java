package com.wdkj.wf.house.entity;

import com.wf.common.latLon.LatLon;

import java.util.List;

/**所属村分类
 * @author 1
 *
 */
public class VillageEntity {
	private Integer id;
	private String name;
	private Integer streetcode;
	private String code;
	private int maxRecord;
	private String points;
	private String street; 		//街道
	private List<LatLon> boundaryPoints;
	private Integer streetId;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getStreetcode() {
		return streetcode;
	}

	public void setStreetcode(Integer streetcode) {
		this.streetcode = streetcode;
	}

	public List<LatLon> getBoundaryPoints() {
		return boundaryPoints;
	}

	public void setBoundaryPoints(List<LatLon> boundryPoints) {
		this.boundaryPoints = boundryPoints;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public int getMaxRecord() {
		return maxRecord;
	}

	public void setMaxRecord(int maxrecord) {
		this.maxRecord = maxrecord;
	}

	public String getPoints() {
		return points;
	}

	public void setPoints(String points) {
		this.points = points;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public Integer getStreetId() {
		return streetId;
	}

	public void setStreetId(Integer streetId) {
		this.streetId = streetId;
	}
}
